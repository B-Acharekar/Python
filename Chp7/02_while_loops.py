# While Loop: Repeats as long as a condition is true

i = 1

while (i < 6): # Loop continues while i is less than 6
    print(i)
    i += 1 # Increment i to eventually stop the loop

'''
Output:
1
2
3
4
5
'''
# Write a program to print 1 to 50 using a while loop.

i = 1

while (i <= 50):
    print(i)
    i += 1


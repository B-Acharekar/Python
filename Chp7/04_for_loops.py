# For Loop: Iterates over a sequence (like numbers, lists, strings, etc.)

for i in range (4): #The range() function in python is used to generate a sequence of number.
    print(i) #prints 0 to 3

# for loop with list
l = [1, 2, 3, 4] 

for i in l: 
    print(i) #prints 1, 2, 3, 4

# For loop with tuple
t = (6, 231, 75,212) 
for i in t:
    print(i) #prints 6, 231, 75, 212

# For loop with string
s = "Hello, World!" # string
for i in s:
    print(i) #prints H, e, l, l, o, ,, W, o, r, l, d, !


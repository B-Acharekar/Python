# Primarily these are the following data types in Python:
# 1. Integers
a = 1 # a is the Integer

# 2. Floating point numbers
b = 2.22 # b is the floating point number

# 3. Strings
c = 'Hello' # c is the string

# 4. Booleans
d = True  # d is the boolean

# 5. None
e = None # e is the none type variable

# Note: In Python, variables are dynamically typed, meaning you don't have to declare the type of a variable before using it.

# Rules for choosing an identifier 

    # 1. It should start with a letter (a to z or A to Z) or an underscore (_).
    # 2. It can contain letters, numbers, or underscores.
    # 3. It should not contain any spaces.
    # 4. It should not be a reserved keyword in Python.

# Example of a valid identifier: my_variable, _variable,seven,One8,etc.

# Example of an invalid identifier: 1variable, variable1, variable@, etc.

